function preventRefreshOnSubmit(){
    document.querySelector('form').addEventListener('submit', function(event){
      event.preventDefault()
    })
}

preventRefreshOnSubmit();

//////////////////////////////////////////////////////////////
// create a variable for input document.querySelector('input')
//////////////////////////////////////////////////////////////
let input = document.querySelector('input');
const ul = document.querySelector('.employee-list');
//////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////
// capture and return the employee name information
//////////////////////////////////////////////////////////////
function retrieveEmployeeInformation() {
    return input.value;
}
///////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////
// function will add a new element to .employee-list.  The li
// (employee) will be created under the ul (employee-list).
// Creating a new employee via the input returned by
// retrieveEmployeeInformation
///////////////////////////////////////////////////////////////
function addNewElementAsLi() {
    let employee = retrieveEmployeeInformation();
    let li = document.createElement('li');
    li.innerHTML = employee;
    return ul.appendChild(li)
}
///////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////
// function will add new li at the event of user submission.
// document.querySelect('form') + eventListener.  DO NOT confuse 'onclick'
// for 'click' as your .addEventListener type
///////////////////////////////////////////////////////////////
function addNewLiOnClick() {
    document.querySelector('form').addEventListener('click', function(event){
        addNewElementAsLi();
        input.value = "";
    });
}
/////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////
// function will clear employee list on click.
/////////////////////////////////////////////////////////////////
function clearEmployeeListOnLinkClick() {
    document.querySelector('a').addEventListener('click', function(event){
        ul.innerHTML = "";
    });
}
